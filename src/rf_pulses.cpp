arma::vec GetMSINC(unsigned int nPoints, double TBW,
		   double flipAngle)
{
    arma::vec time = arma::linspace(-1,1,
				    nPoints);

    arma::vec pulse(nPoints);

    double runningSum = 0;
    for (unsigned int timeIndex = 0 ; timeIndex < nPoints; timeIndex++)
    {
	double pulsePoint = (sin(TBW/2*M_PI*time(timeIndex)+0.00001)
			     /(TBW/2*M_PI*time(timeIndex)+0.00001));

	pulsePoint = pulsePoint*(0.54 + 0.46*cos(M_PI*time(timeIndex)));

	runningSum += pulsePoint;

	pulse(timeIndex) = pulsePoint;
	
    }
    pulse = pulse/runningSum;

    pulse = (flipAngle*M_PI/180.0)*pulse;
    
    return pulse;
}

